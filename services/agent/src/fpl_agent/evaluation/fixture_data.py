"""Snapshot loader for the offline eval framework.

The agent's production tool registry talks to live Neon. For evals we want
deterministic scores — a fresh PL gameweek shouldn't shift the metric. So
the eval substrate snapshots ``player_embeddings`` to a parquet file
committed under ``tests/fixtures/``, and ``fixture_tools.py`` builds a
parallel tool registry that reads from that snapshot in-process.

This module owns:

* :class:`PlayerFixture` — loads the parquet, validates the schema, and
  exposes a pandas DataFrame plus a pre-stacked ``(N, 384)`` embedding
  matrix for fast batched cosine similarity.
* :func:`canned_user_squad` — builds a deterministic 15-player squad from
  the loaded fixture, used by every ``has_user_squad=True`` eval case.
  The selection is top-by-total-points within each position so that
  regenerating the snapshot produces a stable squad layout for scoring.
* :class:`PlayerFixture.synthetic` — a small handcrafted in-memory fixture
  used by framework unit tests that don't need the real snapshot.

The snapshot itself is generated by ``scripts/snapshot_player_db.py``,
which connects to live Neon and asserts every case's
``pinned_roster_facts`` and ``must_mention_players`` are satisfied before
overwriting the committed parquet.
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

import numpy as np
import pandas as pd

from fpl_agent.models.responses import SquadPick, UserSquad

logger = logging.getLogger(__name__)

# Required columns mirror what the production tools query. Snapshot regen
# fails loud if the table schema drifts and any of these are missing.
REQUIRED_COLUMNS: frozenset[str] = frozenset(
    {
        "player_id",
        "embedding",
        "web_name",
        "team_name",
        "position",
        "price",
        "form",
        "total_points",
        "fixture_difficulty",
        "injury_risk_score",
        "form_trend",
        "summary",
    }
)

# Default snapshot path: <repo>/services/agent/tests/fixtures/player_db_v1.parquet.
# Computed relative to this file so the path is stable regardless of CWD.
# parents[0] = evaluation/, [1] = fpl_agent/, [2] = src/, [3] = agent/.
DEFAULT_FIXTURE_PATH: Path = (
    Path(__file__).resolve().parents[3] / "tests" / "fixtures" / "player_db_v1.parquet"
)

# Embedding dimension matches the all-MiniLM-L6-v2 model the production
# enrichment pipeline uses (ADR-0009).
_EMBEDDING_DIM = 384


@dataclass(frozen=True)
class PlayerFixture:
    """In-memory snapshot of ``player_embeddings`` for offline eval runs.

    The DataFrame is the canonical source of truth for tool queries. The
    pre-stacked ``embeddings`` matrix is a derived view for batched cosine
    similarity in :func:`fixture_tools.search_similar_players` — building it
    once at load time avoids re-stacking on every call.

    Frozen by design — the fixture must not mutate during a run, or scores
    drift mid-eval.
    """

    df: pd.DataFrame
    embeddings: np.ndarray = field(repr=False)  # shape: (N, _EMBEDDING_DIM)

    @classmethod
    def from_parquet(cls, path: Path | None = None) -> PlayerFixture:
        """Load and validate a snapshot from disk.

        Raises:
            FileNotFoundError: if the snapshot hasn't been generated yet.
            ValueError: if the schema is missing required columns.
        """
        resolved_path = path or DEFAULT_FIXTURE_PATH
        if not resolved_path.exists():
            raise FileNotFoundError(
                f"Snapshot fixture not found at {resolved_path}. Generate it with "
                f"`python services/agent/scripts/snapshot_player_db.py` "
                f"(requires NEON_DATABASE_URL)."
            )
        df = pd.read_parquet(resolved_path)
        cls._assert_schema(df)
        embeddings = cls._stack_embeddings(df)
        logger.info(
            "Loaded fixture from %s: %d players, %d-dim embeddings",
            resolved_path,
            len(df),
            embeddings.shape[1],
        )
        return cls(df=df.reset_index(drop=True), embeddings=embeddings)

    @classmethod
    def synthetic(cls, *, seed: int = 42) -> PlayerFixture:
        """Build a small in-memory fixture for framework unit tests.

        Includes every player named in :mod:`fpl_agent.evaluation.eval_cases`
        so the cases can run end-to-end against this fixture without needing
        the real snapshot. Numeric fields are plausible but not real.
        Embeddings are random with a fixed seed for determinism.

        Not for real eval runs — those need the parquet snapshot for honest
        scores against actual data.
        """
        rng = np.random.default_rng(seed)
        # Players, position, team, plausible stats. Mirrors the rows the
        # production schema produces, minus the real numbers.
        # injury_risk_score is an integer 0-10 in the production schema (verified
        # against live Neon on 2026-05-22). Earlier versions used 0.0-1.0 floats
        # which silently diverged from real-data behaviour.
        synthetic_rows: list[dict[str, Any]] = [
            # (web_name, position, team, price, form, total_points, fixture_difficulty,
            #  injury_risk_score, form_trend, summary)
            _row("Salah", "MID", "Liverpool", 13.0, 7.4, 180, 2.3, 1, "up", "in form"),
            _row("Saka", "MID", "Arsenal", 10.0, 6.8, 150, 2.6, 5, "stable", "consistent"),
            _row("Palmer", "MID", "Chelsea", 11.0, 7.1, 165, 2.8, 2, "stable", "elite ceiling"),
            _row("Foden", "MID", "Man City", 9.5, 5.2, 110, 2.4, 3, "down", "tactical role uncertain"),
            _row("Grealish", "MID", "Everton", 6.5, 4.2, 70, 3.0, 10, "down", "on loan, persistent injury concerns"),
            _row("Haaland", "FWD", "Man City", 15.0, 8.2, 200, 2.5, 1, "stable", "elite returns"),
            _row("Watkins", "FWD", "Aston Villa", 9.0, 5.8, 130, 3.0, 2, "up", "good run"),
            _row("Isak", "FWD", "Newcastle", 8.5, 6.0, 120, 2.9, 3, "up", "form returning"),
            _row("Saliba", "DEF", "Arsenal", 6.0, 5.5, 110, 2.6, 1, "stable", "defensive rock"),
            _row("Trippier", "DEF", "Newcastle", 6.5, 4.8, 95, 3.1, 2, "stable", "set-piece threat"),
            _row("Robertson", "DEF", "Liverpool", 6.5, 5.0, 100, 2.4, 2, "stable", "attacking output"),
            _row("Virgil", "DEF", "Liverpool", 6.5, 5.8, 120, 2.4, 1, "stable", "stored under first name in FPL — mirrors real schema quirk"),
            _row("Gabriel", "DEF", "Arsenal", 6.0, 5.3, 105, 2.6, 1, "stable", "aerial threat"),
            _row("Estupiñán", "DEF", "Brighton", 5.0, 4.5, 80, 3.3, 3, "down", "minutes risk"),
            _row("Raya", "GKP", "Arsenal", 5.5, 4.8, 95, 2.6, 0, "stable", "first-choice GK"),
            _row("Ederson", "GKP", "Man City", 5.5, 4.5, 88, 2.5, 1, "stable", "elite distribution"),
            _row("Pickford", "GKP", "Everton", 5.0, 4.2, 80, 3.5, 2, "stable", "save-point potential"),
            _row("Sels", "GKP", "Nottingham Forest", 4.5, 4.7, 90, 3.2, 1, "up", "budget option"),
            _row("Cunha", "FWD", "Wolves", 7.0, 4.2, 85, 3.4, 4, "down", "form dipping"),
            _row("Iwobi", "MID", "Fulham", 5.5, 4.8, 80, 3.0, 2, "stable", "low-owned option"),
        ]
        df = pd.DataFrame(synthetic_rows)
        df["player_id"] = range(1, len(df) + 1)
        df["embedding"] = [
            rng.standard_normal(_EMBEDDING_DIM).astype(np.float32).tolist() for _ in range(len(df))
        ]
        cls._assert_schema(df)
        embeddings = cls._stack_embeddings(df)
        return cls(df=df.reset_index(drop=True), embeddings=embeddings)

    @staticmethod
    def _assert_schema(df: pd.DataFrame) -> None:
        missing = REQUIRED_COLUMNS - set(df.columns)
        if missing:
            raise ValueError(
                f"Snapshot is missing required columns: {sorted(missing)}. "
                f"Schema may have drifted — regenerate the snapshot."
            )

    @staticmethod
    def _stack_embeddings(df: pd.DataFrame) -> np.ndarray:
        # parquet round-trip stores embeddings as object dtype (list-of-float).
        # Stack into a contiguous (N, D) float32 array for fast batched maths.
        if df.empty:
            return np.zeros((0, _EMBEDDING_DIM), dtype=np.float32)
        return np.array(df["embedding"].tolist(), dtype=np.float32)

    def player_count(self) -> int:
        """Number of rows in the snapshot."""
        return len(self.df)


def _row(
    web_name: str,
    position: str,
    team_name: str,
    price: float,
    form: float,
    total_points: int,
    fixture_difficulty: float,
    injury_risk_score: int,
    form_trend: str,
    summary: str,
) -> dict[str, Any]:
    """Compact constructor for synthetic-fixture rows. Keeps the synthetic table readable."""
    return {
        "web_name": web_name,
        "position": position,
        "team_name": team_name,
        "price": price,
        "form": form,
        "total_points": total_points,
        "fixture_difficulty": fixture_difficulty,
        "injury_risk_score": injury_risk_score,
        "form_trend": form_trend,
        "summary": summary,
    }


# ---------------------------------------------------------------------------
# Canned UserSquad for squad-aware eval cases
# ---------------------------------------------------------------------------
# Selected by top-total-points within each position so the layout is stable
# across snapshot regenerations (given the same underlying players). The
# squad-aware rubrics anchor on this deterministic selection rather than on
# specific element_ids.

_FPL_POSITION_CODE: dict[str, int] = {"GKP": 1, "DEF": 2, "MID": 3, "FWD": 4}
_FPL_SQUAD_COUNTS: dict[str, int] = {"GKP": 2, "DEF": 5, "MID": 5, "FWD": 3}  # total 15
_FPL_STARTER_COUNTS: dict[str, int] = {"GKP": 1, "DEF": 4, "MID": 4, "FWD": 2}  # total 11


# Players that must appear in every canned squad. Cases that name a specific
# player in the question (e.g. squad-bench-or-start-salah) need that player
# in the squad for the rubric premise to hold — otherwise the agent reasonably
# pivots to a meta-answer ("they're not in your team") and the rubric punishes
# it for that. Discovered during baseline-v1.1 calibration; see docs/evals/baseline.md.
_DEFAULT_MUST_INCLUDE: tuple[str, ...] = ("Salah",)


def canned_user_squad(
    fixture: PlayerFixture,
    *,
    gameweek: int = 10,
    must_include_web_names: tuple[str, ...] = _DEFAULT_MUST_INCLUDE,
) -> UserSquad:
    """Build a deterministic 15-player squad from the fixture.

    Used by every eval case with ``has_user_squad=True``. The selection
    rule — top-N-by-total-points per position — is stable, so a regenerated
    snapshot yields the same squad shape and squad-aware rubrics keep
    scoring consistently.

    Players named in ``must_include_web_names`` are forced into the squad
    regardless of total-points ranking — they displace the lowest-scoring
    selected player in the same position. Used to ensure squad-aware cases
    that name a specific player can actually find them in the squad.

    The canned squad captains the highest-scoring midfielder, vice-captains
    the highest-scoring forward — a common real-user pattern that exercises
    captain-pick eval cases meaningfully. Force-included players are not
    automatically promoted to captain.
    """
    df = fixture.df

    if df.empty:
        raise ValueError("Cannot build a canned UserSquad from an empty fixture.")

    # Pick top-N by total_points for each position
    selected: dict[str, list[dict[str, Any]]] = {}
    for pos_label, count in _FPL_SQUAD_COUNTS.items():
        pos_rows = df[df["position"] == pos_label].nlargest(count, "total_points")
        if len(pos_rows) < count:
            raise ValueError(
                f"Fixture has only {len(pos_rows)} {pos_label} players; need {count} for a "
                f"valid squad. The snapshot is too sparse."
            )
        selected[pos_label] = pos_rows.to_dict("records")

    # Force-include any named players. Place each at index 1 of the position
    # list — that guarantees they end up in the starter section (since the
    # first STARTER_COUNT entries become starters) without displacing the
    # captain at index 0. If the player has more points than the captain, this
    # demotes them to second starter rather than overwriting the captaincy —
    # deliberate, so the rubric for squad-bench-or-start-salah tests the case
    # where the named player is owned and started but not captain.
    for must_name in must_include_web_names:
        match = df[df["web_name"].astype(str).str.contains(must_name, case=False, na=False, regex=False)]
        if match.empty:
            raise ValueError(
                f"must_include_web_names entry {must_name!r} not found in fixture. "
                f"Snapshot may be stale or the must-include list needs updating."
            )
        target = match.nlargest(1, "total_points").iloc[0].to_dict()
        target_pos = str(target["position"])
        target_id = int(target["player_id"])

        # Remove if present (we'll re-insert at the right position) then insert at
        # index 1, then trim to the squad count for this position. Net effect:
        # must-include is the second starter; lowest-points selection is displaced.
        without = [p for p in selected[target_pos] if int(p["player_id"]) != target_id]
        without.sort(key=lambda p: p["total_points"], reverse=True)
        without.insert(1, target)
        selected[target_pos] = without[: _FPL_SQUAD_COUNTS[target_pos]]

    # Captain = highest-scoring midfielder. Vice-captain = highest-scoring forward.
    # Force-included players are NOT promoted to captain — they sit in the squad as
    # regular picks. Deliberate: lets squad-bench-or-start-salah test the case where
    # the named player is owned but not the captain.
    captain_id = int(selected["MID"][0]["player_id"])
    vice_id = int(selected["FWD"][0]["player_id"])

    picks: list[SquadPick] = []
    slot = 1

    # Starters first (1 GK, 4 DEF, 4 MID, 2 FWD)
    for pos_label in ("GKP", "DEF", "MID", "FWD"):
        for row in selected[pos_label][: _FPL_STARTER_COUNTS[pos_label]]:
            element_id = int(row["player_id"])
            is_captain = element_id == captain_id
            is_vice = element_id == vice_id
            picks.append(
                SquadPick(
                    element_id=element_id,
                    web_name=str(row["web_name"]),
                    team_name=str(row["team_name"]),
                    position=slot,
                    element_type=_FPL_POSITION_CODE[pos_label],
                    multiplier=2 if is_captain else 1,
                    is_captain=is_captain,
                    is_vice_captain=is_vice,
                    price=float(row["price"]),
                )
            )
            slot += 1

    # Bench (positions 12–15): GK bench, then outfield bench in the order GK/DEF/MID/FWD
    for pos_label in ("GKP", "DEF", "MID", "FWD"):
        for row in selected[pos_label][_FPL_STARTER_COUNTS[pos_label] :]:
            picks.append(
                SquadPick(
                    element_id=int(row["player_id"]),
                    web_name=str(row["web_name"]),
                    team_name=str(row["team_name"]),
                    position=slot,
                    element_type=_FPL_POSITION_CODE[pos_label],
                    multiplier=0,
                    is_captain=False,
                    is_vice_captain=False,
                    price=float(row["price"]),
                )
            )
            slot += 1

    total_value = round(sum(p.price for p in picks), 1)
    total_points = int(
        sum(int(row["total_points"]) for pos_rows in selected.values() for row in pos_rows)
    )

    return UserSquad(
        team_id=999_999,  # sentinel ID — this is a synthetic squad, not a real FPL team
        gameweek=gameweek,
        picks=picks,
        bank=1.5,
        total_value=total_value,
        active_chip=None,
        overall_rank=None,
        total_points=total_points,
    )
